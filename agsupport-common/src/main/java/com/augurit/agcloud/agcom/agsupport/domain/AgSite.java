public class AgSite {
    private String id;
    private String serName;
    private String serPassword;
    private String serIp;
    private String serPort;
    private String serType;
    private String serAlias;
    private String serRestport;


    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getSerName() {
        return serName;
    }
    public void setSerName(String serName) {
        this.serName = serName;
    }

    public String getSerPassword() {
        return serPassword;
    }

    public void setSerPassword(String serPassword) {
        this.serPassword = serPassword;
    }

    public String getSerIp() {
        return serIp;
    }

    public void setSerIp(String serIp) {
        this.serIp = serIp;
    }

    public String getSerPort() {
        return serPort;
    }

    public void setSerPort(String serPort) {
        this.serPort = serPort;
    }
    public String getSerType() {
        return serType;
    }

    public void setSerType(String serType) {
        this.serType = serType;
    }
    public String getSerAlias() {
        return serAlias;
    }

    public void setSerAlias(String serAlias) {
        this.serAlias = serAlias;
    }
    public String getSerRestport() {
        return serRestport;
    }

    public void setSerRestport(String serRestport) {
        this.serRestport = serRestport;
    }
}
